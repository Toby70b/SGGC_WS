package com.sggc.models;

import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAttribute;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAutoGeneratedKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBHashKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBTable;
import lombok.AllArgsConstructor;
import lombok.Data;
import lombok.RequiredArgsConstructor;
import org.springframework.data.annotation.Id;

import java.util.Set;

/**
 * Represents a response to the SGGC controller to retrieve common games
 */
@Data
@DynamoDBTable(tableName = "User")
@RequiredArgsConstructor
@AllArgsConstructor
public class User {

    @Id
    private String id;

    private Set<String> ownedGameIds;

    @DynamoDBHashKey
    @DynamoDBAutoGeneratedKey
    public String getId() {
        return id;
    }

    @DynamoDBAttribute
    public Set<String> getOwnedGameIds() {
        return ownedGameIds;
    }

    //TTL (time-to-live) field for DynamoDB
    private long removalDate;
}
